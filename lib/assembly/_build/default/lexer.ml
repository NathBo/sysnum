    let convert_to_base_2 n = 
        let rec aux cur_val cur_bit = match (cur_val, cur_bit) with
            | (0, 16) -> ""
            | (_, 16) -> raise (Lexing_error ("Overflow in constant"))
            | (0, _) -> "0" ^ (aux 0 (cur_bit + 1))
            | (v, _) ->  (if v mod 2 = 0 then "0"
                        else "1") ^ (aux (v / 2) (cur_bit + 1)) in
        aux n 0

